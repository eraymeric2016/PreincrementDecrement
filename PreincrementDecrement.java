package week_04;

public class PreincrementDecrement {
    public static void main(String[] args) {

        int a=10;

        int b=++a;

        System.out.println(b);

        System.out.println("--------------------");


        int c=--b;


        System.out.println(b--);
        System.out.println("--------------------");
        System.out.println("b=" + b);
        System.out.println("--------------------");
        System.out.println(c);
        System.out.println("--------------------");
        System.out.println(a++ + b++ + c);


















    }
}
